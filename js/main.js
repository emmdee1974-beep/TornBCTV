function cardHTML(item){
  const thumb = `https://i.ytimg.com/vi/${item.id}/hqdefault.jpg`;
  return `
  <article class="card">
    <a class="card__media" href="${item.url}" target="_blank">
      <span class="card__tag">${item.tag}</span>
      <img src="${thumb}" />
    </a>
    <div class="card__body">
      <h3 class="card__title">${item.title}</h3>
      <p class="card__desc">${item.desc}</p>
    </div>
  </article>`;
}

function mount(section, id){
  const el = document.getElementById(id);
  if(el) el.innerHTML = section.map(cardHTML).join("");
}

document.addEventListener("DOMContentLoaded", ()=>{
  mount(lists.news, "latestCards");
  mount(lists.ads, "adRail");
  mount(lists.promos, "topCards");
  mount(lists.markets, "marketCards");
  mount(lists.community, "communityCards");
});
